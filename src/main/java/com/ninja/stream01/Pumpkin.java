package com.ninja.stream01;

public class Pumpkin {

    private final int size;
    private final int radius;

    public Pumpkin(int size, int radius) {
        this.size = size;
        this.radius = radius;
    }

    public int getSize() {
        return size;
    }

    public int getRadius() {
        return radius;
    }


}
